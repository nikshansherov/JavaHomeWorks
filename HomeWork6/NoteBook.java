public class NoteBook {
    String name;
    String ozu;
    String gmd;
    String os;
    String color;

    NoteBook(String name, String ozu, String gmd, String os, String color) {
        this.name = name;
        this.ozu = ozu;
        this.gmd = gmd;
        this.os = os;
        this.color = color;
    }
}
